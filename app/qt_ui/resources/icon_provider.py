def get_icon(name: str, color: str = "#EAEAEA") -> QIcon:
    """
    Creates a QIcon from embedded SVG data, with a specified color.
    This method is robust against filesystem issues and allows for dynamic
    coloring of icons to match the theme.
    """
    svg_data = ICON_DATA.get(name)
    if not svg_data:
        logging.warning(f"Icon '{name}' not found in icon_provider.")
        return QIcon()

    # Replace 'currentColor' with the desired hex color
    colored_svg = svg_data.replace('stroke="currentColor"', f'stroke="{color}"')
    
    byte_array = QByteArray(colored_svg.encode('utf-8'))
    pixmap = QPixmap()
    pixmap.loadFromData(byte_array, 'SVG')
    
    return QIcon(pixmap) 